//Popup einblenden
function info_popup() {
	document.getElementById('info_modal').style.display='block';
	document.getElementById('info_link').style.background='darkgreen';
}
//Popup ausblenden
function info_popup_off() {
	document.getElementById('info_modal').style.display='none';
	document.getElementById('info_link').style.background='green';
}
